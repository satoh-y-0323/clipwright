def _make_large_whisper_json(n_words: int) -> dict[str, Any]:
    """Build a synthetic whisper JSON with n_words valid word tokens.

    Each token starts with a leading space (= new word boundary) and has
    non-degenerate offsets.  Used for CWE-400 boundary tests.
    """
    tokens: list[dict[str, Any]] = []
    for i in range(n_words):
        tokens.append(
            {
                "text": f" w{i}",
                "offsets": {"from": i * 100, "to": (i + 1) * 100},
                "id": 100 + (i % 1000),
                "p": 0.9,
                "t_dtw": -1,
            }
        )
    return {
        "transcription": [
            {
                "offsets": {"from": 0, "to": n_words * 100},
                "text": "many words",
                "tokens": tokens,
            }
        ]
    }


