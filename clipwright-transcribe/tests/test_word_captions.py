class TestCWE400MaxWords:
    """Verify extract_word_segments raises INVALID_INPUT when word count > 50_000.

    ADR-K8: MAX_WORDS_TRANSCRIBE = 50_000
    Headroom: 30-min short ≈ 5,400 words (3 word/s) → ~10x safety margin.
    50_000 words ≈ 2 MB ASS when rendered — safe upper bound.
    """

    def test_max_words_constant_value(self) -> None:
        """MAX_WORDS_TRANSCRIBE must equal 50_000 (drift guard, ADR-K8)."""
        assert MAX_WORDS_TRANSCRIBE == 50_000

    def test_exceeds_max_words_raises_invalid_input(self) -> None:
        """50_001 valid words → ClipwrightError with code INVALID_INPUT (CWE-400)."""
        large_json = _make_large_whisper_json(50_001)
        with pytest.raises(ClipwrightError) as exc_info:
            extract_word_segments(large_json)
        assert exc_info.value.code == ErrorCode.INVALID_INPUT

    def test_hint_contains_limit_value(self) -> None:
        """The error hint must mention the 50_000 upper limit (AC-6 / ADR-K8).

        The hint should also include guidance to split the input.
        """
        large_json = _make_large_whisper_json(50_001)
        with pytest.raises(ClipwrightError) as exc_info:
            extract_word_segments(large_json)
        hint = exc_info.value.hint
        assert "50000" in hint or "50_000" in hint, (
            f"Hint does not mention the 50_000 word limit: {hint!r}"
        )

    def test_exactly_max_words_does_not_raise(self) -> None:
        """Exactly 50_000 words is within the limit and must not raise (boundary value)."""
        exact_json = _make_large_whisper_json(50_000)
        result = extract_word_segments(exact_json)
        assert len(result) == 1
        assert len(result[0]["words"]) == 50_000
