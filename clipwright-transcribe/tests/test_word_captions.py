class TestExtractWordSegments:
    """Verify extract_word_segments reconstructs words from whisper_word_sample.json.

    The fixture has 3 segments that each exercise one reconstruction rule:
      seg1 (" Okay.")          — [_BEG_] special-token filtering
      seg2 (" I'm going …")    — BPE sub-word merge (' I' + "'m" → "I'm")
      seg3 (" Oh, I'm going…") — degenerate interval (offsets from == to) exclusion
    """

    def test_returns_list(self, whisper_word_sample_json: dict[str, Any]) -> None:
        """extract_word_segments returns a list (AC-1 / F-T-02)."""
        result = extract_word_segments(whisper_word_sample_json)
        assert isinstance(result, list)

    def test_segment_count_three(
        self, whisper_word_sample_json: dict[str, Any]
    ) -> None:
        """All 3 fixture segments (each has start_sec < end_sec) are preserved."""
        result = extract_word_segments(whisper_word_sample_json)
        assert len(result) == 3

    # -----------------------------------------------------------------------
    # Segment 1: " Okay." (0–1000 ms)
    # [_BEG_] special-token excluded; BPE "." merged onto " Okay"
    # -----------------------------------------------------------------------

    def test_segment1_word_count(
        self, whisper_word_sample_json: dict[str, Any]
    ) -> None:
        """Segment 1 yields 1 word ('Okay.' — BPE '.' joined to ' Okay')."""
        result = extract_word_segments(whisper_word_sample_json)
        assert len(result[0]["words"]) == 1

    def test_segment1_word_text(self, whisper_word_sample_json: dict[str, Any]) -> None:
        """Segment 1 word text is 'Okay.' (leading space stripped; BPE '.' merged)."""
        result = extract_word_segments(whisper_word_sample_json)
        assert result[0]["words"][0]["text"] == "Okay."

    def test_segment1_word_start_sec(
        self, whisper_word_sample_json: dict[str, Any]
    ) -> None:
        """'Okay.' word.start_sec = offsets.from of ' Okay' token / 1000 = 0.0 s."""
        result = extract_word_segments(whisper_word_sample_json)
        assert result[0]["words"][0]["start_sec"] == pytest.approx(0.0)

    def test_segment1_word_end_sec(
        self, whisper_word_sample_json: dict[str, Any]
    ) -> None:
        """'Okay.' word.end_sec = offsets.to of '.' token / 1000 = 0.990 s."""
        result = extract_word_segments(whisper_word_sample_json)
        assert result[0]["words"][0]["end_sec"] == pytest.approx(0.990)

    def test_special_token_beg_excluded(
        self, whisper_word_sample_json: dict[str, Any]
    ) -> None:
        """[_BEG_] token (text.startswith('[_')) must not appear in any word text."""
        result = extract_word_segments(whisper_word_sample_json)
        all_texts = [w["text"] for seg in result for w in seg["words"]]
        assert not any("[_BEG_]" in t for t in all_texts)

    def test_special_token_tt_excluded(
        self, whisper_word_sample_json: dict[str, Any]
    ) -> None:
        """[_TT_N] timestamp tokens (text.startswith('[_')) must not appear in any word."""
        result = extract_word_segments(whisper_word_sample_json)
        all_texts = [w["text"] for seg in result for w in seg["words"]]
        assert not any(t.startswith("[_") for t in all_texts)

    # -----------------------------------------------------------------------
    # Segment 2: " I'm going to go." (1000–2000 ms)
    # BPE sub-word: ' I' (new word) + "'m" (no leading space → same word) → "I'm"
    # -----------------------------------------------------------------------

    def test_bpe_subword_merged_into_one_word(
        self, whisper_word_sample_json: dict[str, Any]
    ) -> None:
        """' I' + \"'m\" (no leading space) must merge into 'I'm', not stay separate."""
        result = extract_word_segments(whisper_word_sample_json)
        seg2_texts = [w["text"] for w in result[1]["words"]]
        assert "'m" not in seg2_texts, 'BPE sub-word "\'m" must be merged, not separate'
        assert "I'm" in seg2_texts

    def test_segment2_word_count(
        self, whisper_word_sample_json: dict[str, Any]
    ) -> None:
        """Segment 2 has 4 words: 'I'm' / 'going' / 'to' / 'go.'."""
        result = extract_word_segments(whisper_word_sample_json)
        assert len(result[1]["words"]) == 4

    def test_segment2_words_text_in_order(
        self, whisper_word_sample_json: dict[str, Any]
    ) -> None:
        """Segment 2 word texts are [\"I'm\", 'going', 'to', 'go.'] in order."""
        result = extract_word_segments(whisper_word_sample_json)
        seg2_texts = [w["text"] for w in result[1]["words"]]
        assert seg2_texts == ["I'm", "going", "to", "go."]

    def test_segment2_first_word_start_sec(
        self, whisper_word_sample_json: dict[str, Any]
    ) -> None:
        """'I'm' word.start_sec = offsets.from of ' I' token / 1000 = 1.0 s."""
        result = extract_word_segments(whisper_word_sample_json)
        assert result[1]["words"][0]["start_sec"] == pytest.approx(1.0)

    def test_segment2_first_word_end_sec(
        self, whisper_word_sample_json: dict[str, Any]
    ) -> None:
        """'I'm' word.end_sec = offsets.to of \"'m\" token / 1000 = 1.190 s."""
        result = extract_word_segments(whisper_word_sample_json)
        assert result[1]["words"][0]["end_sec"] == pytest.approx(1.190)

    # -----------------------------------------------------------------------
    # Segment 3: " Oh, I'm going to come." (3000–4000 ms)
    # Degenerate interval: tokens at 4000-4000 ms must be excluded
    # -----------------------------------------------------------------------

    def test_degenerate_tokens_excluded_segment3(
        self, whisper_word_sample_json: dict[str, Any]
    ) -> None:
        """Tokens with offsets.from >= offsets.to (4000-4000 ms) must not become words.

        'going', 'to', 'come' are all at 4000-4000 ms (degenerate) in the fixture
        and must be excluded from the word list.
        """
        result = extract_word_segments(whisper_word_sample_json)
        seg3_texts = [w["text"] for w in result[2]["words"]]
        assert "going" not in seg3_texts
        assert "to" not in seg3_texts
        assert "come" not in seg3_texts

    def test_segment3_word_count(
        self, whisper_word_sample_json: dict[str, Any]
    ) -> None:
        """Segment 3 has 2 words after degenerate filtering: 'Oh,' and 'I'."""
        result = extract_word_segments(whisper_word_sample_json)
        assert len(result[2]["words"]) == 2

    def test_segment3_words_text_in_order(
        self, whisper_word_sample_json: dict[str, Any]
    ) -> None:
        """Segment 3 word texts are ['Oh,', 'I'] (degenerate tail excluded)."""
        result = extract_word_segments(whisper_word_sample_json)
        seg3_texts = [w["text"] for w in result[2]["words"]]
        assert seg3_texts == ["Oh,", "I"]

    # -----------------------------------------------------------------------
    # WordSegment structure — start_sec / end_sec / text from segment-level offsets
    # -----------------------------------------------------------------------

    def test_segment_start_sec_from_offsets(
        self, whisper_word_sample_json: dict[str, Any]
    ) -> None:
        """WordSegment.start_sec = segment offsets.from / 1000."""
        result = extract_word_segments(whisper_word_sample_json)
        assert result[0]["start_sec"] == pytest.approx(0.0)  # seg1: 0 ms
        assert result[1]["start_sec"] == pytest.approx(1.0)  # seg2: 1000 ms
        assert result[2]["start_sec"] == pytest.approx(3.0)  # seg3: 3000 ms

    def test_segment_end_sec_from_offsets(
        self, whisper_word_sample_json: dict[str, Any]
    ) -> None:
        """WordSegment.end_sec = segment offsets.to / 1000."""
        result = extract_word_segments(whisper_word_sample_json)
        assert result[0]["end_sec"] == pytest.approx(1.0)  # seg1: 1000 ms
        assert result[1]["end_sec"] == pytest.approx(2.0)  # seg2: 2000 ms
        assert result[2]["end_sec"] == pytest.approx(4.0)  # seg3: 4000 ms

    def test_segment_text_stripped(
        self, whisper_word_sample_json: dict[str, Any]
    ) -> None:
        """WordSegment.text is stripped (mirrors normalize_segments behaviour)."""
        result = extract_word_segments(whisper_word_sample_json)
        assert result[0]["text"] == "Okay."
        assert result[1]["text"] == "I'm going to go."
        assert result[2]["text"] == "Oh, I'm going to come."

    def test_empty_transcription_returns_empty_list(self) -> None:
        """extract_word_segments with empty transcription list returns []."""
        result = extract_word_segments({"transcription": []})
        assert result == []


