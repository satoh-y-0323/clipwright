class TestToWordVtt:
    """Verify to_word_vtt output format (AC-1 / F-T-03)."""

    def test_starts_with_webvtt_header(self) -> None:
        """Output starts with 'WEBVTT' header (WebVTT spec)."""
        vtt = to_word_vtt(CANONICAL_WORD_SEGMENTS)
        assert vtt.startswith("WEBVTT")

    def test_one_cue_per_segment(self) -> None:
        """Each WordSegment produces exactly one VTT cue (one '-->' per segment)."""
        vtt = to_word_vtt(CANONICAL_WORD_SEGMENTS)
        assert vtt.count("-->") == len(CANONICAL_WORD_SEGMENTS)

    def test_cue_timecode_is_segment_range(self) -> None:
        """Cue start/end timecode matches WordSegment.start_sec / end_sec (AC-1)."""
        vtt = to_word_vtt(CANONICAL_WORD_SEGMENTS)
        # seg1: 1.0 s → 3.5 s
        assert "00:00:01.000 --> 00:00:03.500" in vtt
        # seg2: 4.0 s → 6.2 s
        assert "00:00:04.000 --> 00:00:06.200" in vtt

    def test_inline_timestamp_pattern(self) -> None:
        """Inline timestamps match the <HH:MM:SS.mmm> pattern (architecture §2-1)."""
        import re

        vtt = to_word_vtt(CANONICAL_WORD_SEGMENTS)
        inline_ts_re = re.compile(r"<\d{2}:\d{2}:\d{2}\.\d{3}>")
        assert inline_ts_re.search(vtt), (
            f"No inline timestamp <HH:MM:SS.mmm> found in VTT output: {vtt!r}"
        )

    def test_inline_timestamps_monotonically_increasing(self) -> None:
        """All inline timestamps in the document are monotonically non-decreasing (AC-1)."""
        import re

        vtt = to_word_vtt(CANONICAL_WORD_SEGMENTS)
        ts_re = re.compile(r"<(\d{2}:\d{2}:\d{2}\.\d{3})>")
        timestamps = ts_re.findall(vtt)
        assert len(timestamps) > 0, "No inline timestamps found"
        for prev, curr in zip(timestamps, timestamps[1:], strict=False):
            assert prev <= curr, (
                f"Inline timestamps not monotonically increasing: {prev!r} > {curr!r}"
            )

    def test_ms_separator_is_dot(self) -> None:
        """Timecodes use '.' millisecond separator (not ',') — _format_timecode
        ms_separator='.' consistency (DC-AS-005 / architecture §2-1)."""
        import re

        vtt = to_word_vtt(CANONICAL_WORD_SEGMENTS)
        # Cue header must have dot-separated timecode
        assert re.search(r"\d{2}:\d{2}:\d{2}\.\d{3} -->", vtt)
        # Must NOT contain SRT-style comma separator
        assert not re.search(r"\d{2}:\d{2}:\d{2},\d{3}", vtt)

    def test_empty_word_segments_returns_header_only(self) -> None:
        """to_word_vtt([]) returns output containing only the WEBVTT header (DC-GP-002 parity)."""
        vtt = to_word_vtt([])
        assert "WEBVTT" in vtt
        assert "-->" not in vtt


